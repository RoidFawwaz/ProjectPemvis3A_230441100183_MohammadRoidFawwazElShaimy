    private void tblNilaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNilaiMouseClicked
        // TODO add your handling code here:
         int row = tblNilai.getSelectedRow(); 
        
        if (row != -1) {
            txtNim.setText(tblNilai.getValueAt(row, 1).toString());
            txtNama.setText(tblNilai.getValueAt(row, 2).toString()); 
            String kelamin = tblNilai.getValueAt(row, 3).toString();
            if ("LAKI-LAKI".equals(kelamin)) {
                cbKelamin.setSelectedIndex(1); 
            } else {
                cbKelamin.setSelectedIndex(2);
            }
            String matkul = tblNilai.getValueAt(row, 4).toString();
            if ("RISET OPERASI".equals(matkul)) {
                cbMatkul.setSelectedIndex(1);
            }else if ("ANALISA DAN DESAIN SISTEM INFORMASI".equals(matkul)) {
                cbMatkul.setSelectedIndex(2);
            }else if("REKAYASA PERANGKAT LUNAK".equals(matkul)){
                cbMatkul.setSelectedIndex(3);
            }else if("INTERAKSI MANUSIA DAN KOMPUTER".equals(matkul)){
                cbMatkul.setSelectedIndex(4);
            }else if("ARSITEKTUR SI/TI PERUSAHAAN".equals(matkul)){
                cbMatkul.setSelectedIndex(5);
            }else if("KEAMANAN INFORMASI".equals(matkul)){
                cbMatkul.setSelectedIndex(6);
            } else if("BAHASA INDONESIA".equals(matkul)){
                cbMatkul.setSelectedIndex(7);
            } else if("KEWARGANEGARAAN".equals(matkul)){
                cbMatkul.setSelectedIndex(8);
            }
            String semester = tblNilai.getValueAt(row, 5).toString();
            if ("Semester 1".equals(semester)) {
                cbSemester.setSelectedIndex(1);
            }else if ("Semester 3".equals(semester)){
                cbSemester.setSelectedIndex(2);
            }else if ("Semester 5".equals(semester)){
                cbSemester.setSelectedIndex(3);
            }else if ("Semester 7".equals(semester)){
                cbSemester.setSelectedIndex(4);
            }
            txtAkhir.setText(tblNilai.getValueAt(row, 6).toString());
            txtPredikat.setText(tblNilai.getValueAt(row, 7).toString());
        }
    }//GEN-LAST:event_tblNilaiMouseClicked

