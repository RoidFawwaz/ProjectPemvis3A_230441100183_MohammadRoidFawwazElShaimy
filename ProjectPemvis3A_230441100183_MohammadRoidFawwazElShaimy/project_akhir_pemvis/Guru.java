    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
          try {
        // Ambil nilai dari input pengguna
        double tugas = Double.parseDouble(txtTugas.getText());
        double kuis = Double.parseDouble(txtKuis.getText());
        double uts = Double.parseDouble(txtUts.getText());
        double uas = Double.parseDouble(txtUas.getText());
        
        // Hitung nilai akhir
        double nilaiAkhir = hitungNilaiAkhir(tugas, kuis, uts, uas);
        
        // Tentukan predikat
        String predikat = tentukanPredikat(nilaiAkhir);
        
        // Tampilkan hasil di form (gunakan label atau text field di form Anda)
        txtAkhir.setText(String.format("%.2f", nilaiAkhir));
        txtPredikat.setText(predikat);
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan nilai yang valid!");
    }

    }//GEN-LAST:event_btnHitungActionPerformed

