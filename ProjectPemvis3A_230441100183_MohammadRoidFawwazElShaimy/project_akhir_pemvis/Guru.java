    private double hitungNilaiAkhir(double tugas, double kuis, double uts, double uas) {
    return (0.2 * tugas) + (0.2 * kuis) + (0.25 * uts) + (0.35 * uas);
}

    private String tentukanPredikat(double nilaiAkhir) {
        if (nilaiAkhir >= 85) {
            return "A";
        } else if (nilaiAkhir >= 75) {
            return "B";
        } else if (nilaiAkhir >= 65) {
            return "C";
        } else if (nilaiAkhir >= 50) {
            return "D";
        } else {
            return "E";
        }
}
