    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
       try {
    // Ambil data dari form
    String nim = txtNim.getText(); // Nim
    String nama = txtNama.getText(); // Nama
    String kelamin = cbKelamin.getSelectedItem().toString(); // Kelamin
    String matakuliah = cbMatkul.getSelectedItem().toString(); // Matakuliah
    String semester = cbSemester.getSelectedItem().toString(); // Semester

    // Nilai akhir
    double nilaiAkhir = Double.parseDouble(txtAkhir.getText()); // Nilai akhir
    String predikat = tentukanPredikat(nilaiAkhir); // Tentukan predikat berdasarkan nilai akhir

    // SQL query (semua kolom wajib diisi)
    String sql = "INSERT INTO nilai (Nim, Nama, Kelamin, Matakuliah, semester, nilai_akhir, predikat) "
               + "VALUES (?, ?, ?, ?, ?, ?, ?)";
    java.sql.Connection Conn = (Connection) koneksi.getConnection();
    java.sql.PreparedStatement pst = Conn.prepareStatement(sql);

    // Isi parameter sesuai urutan kolom di SQL
    pst.setString(1, nim);          // Nim
    pst.setString(2, nama);         // Nama
    pst.setString(3, kelamin);      // Kelamin
    pst.setString(4, matakuliah);   // Matakuliah
    pst.setString(5, semester);     // Semester
    pst.setDouble(6, nilaiAkhir);   // nilai_akhir
    pst.setString(7, predikat);     // predikat

    // Eksekusi query
    pst.execute();

    // Notifikasi sukses
    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
    load_data(); // Refresh tabel setelah menyimpan
    } catch (Exception e) {
    // Tampilkan error jika terjadi masalah
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    System.out.println("Error: " + e.getMessage());
    }



    }//GEN-LAST:event_btnSimpanActionPerformed

