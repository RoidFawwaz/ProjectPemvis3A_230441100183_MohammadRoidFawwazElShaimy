    private void tblDosenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDosenMouseClicked
        // TODO add your handling code here:
         int row = tblDosen.getSelectedRow(); 
        
        if (row != -1) {
            txtNip.setText(tblDosen.getValueAt(row, 1).toString());
            txtNamaDosen.setText(tblDosen.getValueAt(row, 2).toString()); 
            String kelamin = tblDosen.getValueAt(row, 3).toString();
            if ("Laki - Laki".equals(kelamin)) {
                cboKelaminDosen.setSelectedIndex(1); 
            } else {
                cboKelaminDosen.setSelectedIndex(2);
            }
            txtEmailDosen.setText(tblDosen.getValueAt(row, 4).toString());
        }
    }//GEN-LAST:event_tblDosenMouseClicked

