    private void tblMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMahasiswaMouseClicked
        // TODO add your handling code here:
        int row = tblMahasiswa.getSelectedRow(); 
        
        if (row != -1) {
            txtNim.setText(tblMahasiswa.getValueAt(row, 1).toString());
            txtNamaMahasiswa.setText(tblMahasiswa.getValueAt(row, 2).toString()); 
            String kelamin = tblMahasiswa.getValueAt(row, 3).toString();
            if ("Laki - Laki".equals(kelamin)) {
                cboKelaminMahasiswa.setSelectedIndex(1); 
            } else {
                cboKelaminMahasiswa.setSelectedIndex(2);
            }
            txtEmailMahasiswa.setText(tblMahasiswa.getValueAt(row, 4).toString());
        }
    }//GEN-LAST:event_tblMahasiswaMouseClicked

