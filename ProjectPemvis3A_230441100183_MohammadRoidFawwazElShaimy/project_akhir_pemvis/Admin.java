    private void btnCariDosenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariDosenActionPerformed
        // TODO add your handling code here:
        try {
            int no = 1;
            String sql = "SELECT * FROM dosen WHERE nip = ?";
            java.sql.Connection conn = (Connection) koneksi.getConnection();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, txtCariDosen.getText());
            java.sql.ResultSet res = pst.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tblDosen.getModel();
            model.setRowCount(0);

            while (res.next()) {
                Object[] row = {
                    no++,
                    res.getString("nip"),
                    res.getString("nama_dosen"),
                    res.getString("jenis_kelamin"),
                    res.getString("email")
                };
                model.addRow(row);
                txtNip.setText(res.getString("nip"));
                txtNamaDosen.setText(res.getString("nama_dosen"));
                String kelamin = res.getString("jenis_kelamin");
                cboKelaminDosen.setSelectedItem(kelamin);
                txtEmailDosen.setText(res.getString("email"));
            }

            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }


    }//GEN-LAST:event_btnCariDosenActionPerformed

